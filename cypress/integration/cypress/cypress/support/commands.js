Cypress.Commands.add('failTest', (message) => {
    // Use Cypress expect to throw an error
    cy.once('fail', (error) => { throw error });
    cy.once('uncaught:exception', (error) => { throw error });
  
    // Throw an error with the provided message
    throw new Error(message);
  });
  